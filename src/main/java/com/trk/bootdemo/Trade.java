package com.trk.bootdemo;

public class Trade {
	public String id;
	public double qty;
	public double price;
	public String getId() {return id;}
	public void setId(String id) {
		this.id = id;
	}
	public double getQty() {
		return qty;
	}
	public void setQty(double qty) {
		this.qty = qty;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	
	
}
